const getOpponentId = (fighterId: FighterId): FighterId =>
  fighterId === "red" ? "blue" : "red";

const keypoint = (id: string, label: string, x: number, y: number): PoseKeypoint => ({
  id,
  label,
  x,
  y,
});

const getHighlightedJoints = (movementType: MovementType): string[] => {
  if (["jab", "cross", "hook", "uppercut", "elbow", "counter"].includes(movementType)) {
    return ["lead_hand", "rear_hand", "lead_elbow", "rear_elbow", "head"];
  }

  if (["low_kick", "body_kick", "head_kick", "knee"].includes(movementType)) {
    return ["lead_knee", "rear_knee", "lead_foot", "rear_foot", "hip"];
  }

  if (
    ["takedown_attempt", "successful_takedown", "clinch_exchange", "scramble"].includes(
      movementType,
    )
  ) {
    return ["neck", "lead_shoulder", "rear_shoulder", "hip", "lead_knee"];
  }

  return ["head", "neck", "hip"];
};

const buildPoseKeypoints = (
  movementType: MovementType,
  fighterId: FighterId,
): PoseKeypoint[] => {
  const facingRight = fighterId === "red";
  const punchExtension = ["jab", "cross", "hook", "uppercut", "counter", "elbow"].includes(
    movementType,
  );
  const kickExtension = ["low_kick", "body_kick", "head_kick", "knee"].includes(
    movementType,
  );
  const grappling = [
    "takedown_attempt",
    "successful_takedown",
    "clinch_exchange",
    "scramble",
  ].includes(movementType);
  const defensive = movementType === "defensive_movement";
  const direction = facingRight ? 1 : -1;
  const centerX = 50;
  const crouch = grappling ? 9 : defensive ? 5 : 0;
  const shoulderY = 31 + crouch;
  const hipY = 58 + crouch;
  const leadX = centerX + direction * 10;
  const rearX = centerX - direction * 10;
  const extendedHandX = punchExtension ? leadX + direction * 18 : leadX + direction * 4;
  const extendedFootX = kickExtension ? leadX + direction * 20 : leadX + direction * 4;
  const kickFootY =
    movementType === "head_kick"
      ? 29
      : movementType === "body_kick" || movementType === "knee"
        ? 48
        : 78;

  return [
    keypoint("head", "Head", centerX + direction * 2, 18 + crouch * 0.4),
    keypoint("neck", "Neck", centerX, 27 + crouch),
    keypoint("lead_shoulder", "Lead shoulder", leadX, shoulderY),
    keypoint("rear_shoulder", "Rear shoulder", rearX, shoulderY + 2),
    keypoint("lead_elbow", "Lead elbow", punchExtension ? leadX + direction * 12 : leadX, 42),
    keypoint("rear_elbow", "Rear elbow", rearX - direction * 2, 43 + crouch * 0.4),
    keypoint("lead_hand", "Lead hand", extendedHandX, punchExtension ? 36 : 48),
    keypoint("rear_hand", "Rear hand", rearX + direction * 2, defensive ? 30 : 47),
    keypoint("hip", "Hip", centerX, hipY),
    keypoint("lead_knee", "Lead knee", leadX + direction * 4, kickExtension ? 62 : 74),
    keypoint("rear_knee", "Rear knee", rearX - direction * 4, grappling ? 72 : 75),
    keypoint("lead_foot", "Lead foot", extendedFootX, kickExtension ? kickFootY : 90),
    keypoint("rear_foot", "Rear foot", rearX - direction * 7, grappling ? 88 : 91),
  ];
};

export const createPoseFrame = (event: FightEvent): PoseFrame => ({
  id: event.poseFrameId,
  timestamp: event.timestamp,
  fighterId: event.fighterId,
  movementType: event.movementType,
  keypoints: buildPoseKeypoints(event.movementType, event.fighterId),
  highlightedJoints: getHighlightedJoints(event.movementType),
  facing: event.fighterId === "red" ? "right" : "left",
});

